#include <stdio.h>

int main() {
    int A, B, SOMA;

    // Read the two integer values A and B
    scanf("%d", &A);
    scanf("%d", &B);

    // Calculate the sum and store it in SOMA
    SOMA = A + B;

    // Print the result with the required format
    printf("SOMA = %d\n", SOMA);

    return 0;
}

